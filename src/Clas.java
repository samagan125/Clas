public class Clas {
    private int san;
    private String soz;
    private int[] massiv;

    public Clas(int san, String soz, int[] massiv) {
        this.san = san;
        this.soz = soz;
        this.massiv = massiv;
    }

    public void setSan(int san) {
        this.san = san;
    }

    public void setSoz(String soz) {
        this.soz = soz;
    }

    public void setMasiv(int[] masiv) {
        this.massiv = masiv;
    }

    public int getSan() {
        return san;
    }

    public String getSoz() {
        return soz;
    }

    public int[] getMasiv() {
        return massiv;
    }
}




